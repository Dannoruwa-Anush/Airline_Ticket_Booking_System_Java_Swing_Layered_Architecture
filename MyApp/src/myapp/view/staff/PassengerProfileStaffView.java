    private void loadPassengerProfilesIntoTable(List<PassengerProfileResponseDTO> passengerProfiles) {
        String[] columns = {"role_Id", "No", "Name", "Passport No", "Nationality", "Contact No", "Emergency Contact No"};

        paginatedUI = new PaginatedTableUI<>(
                tbl_passengerProfileList,
                columns,
                
                // pass a function (lambda) | This function will take single 'ObjResponseDTO' object into a String array
                passenger -> new String[]{
                    String.valueOf(passenger.getPassengerId()),
                    String.valueOf(passengerProfiles.indexOf(passenger) + 1),
                    passenger.getPassengerFullName(),
                    passenger.getPassengerPassportNumber(),
                    passenger.getPassengerNationality(),
                    passenger.getPassengerContactNumber(),
                    passenger.getPassengerEmergencyContactNumber()
                },
                10 //page size
        );

        paginatedUI.setData(passengerProfiles);
    }

    private void loadAllPassengerProfiles() {
        try {
            List<PassengerProfileResponseDTO> passengers = passengerProfileController.getAllPassengerProfiles();
            loadPassengerProfilesIntoTable(passengers);
        } catch (Exception ex) {
            AuditUtil.logException(this.getClass(), ex);
            CommonMessageBoxUI.showOperationFailMessageBox(this, "Failed to load passenger profiles.");
        }
    }

    private void loadAllSearchKeyMatchPassengerProfiles(String searchKey) {
        try {
            List<PassengerProfileResponseDTO> passengers = passengerProfileController.getAllPassengerProfilesBySearchKey(searchKey);
            loadPassengerProfilesIntoTable(passengers);
        } catch (Exception ex) {
            AuditUtil.logException(this.getClass(), ex);
            CommonMessageBoxUI.showOperationFailMessageBox(this, "Failed to load search results.");
        }
    }

    private void handleSearch() {
        String searchKey = txt_searchBar.getText().trim();
        if (!searchKey.isEmpty()) {
            loadAllSearchKeyMatchPassengerProfiles(searchKey);
        } else {
            loadAllPassengerProfiles();
        }
    }
    //-------------------||||||| End : load for table |||||| -------------------
    
